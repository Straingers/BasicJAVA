package e_oop;

public class Calculator {
	//산술연산 + - * / % 메서드
	//각각의 메서드는 2개의 값을 받아서 연산을 하고 리턴을 받으시오.
	
	double hap(double a, double b){
		return a + b;
	}
	
	double gob(double a, double b){
		return a * b;
	}
	
	double slash(double a, double b){
		return a / b;
	}
	
	double cha(double a, double b){
		return a - b;
	}
	
	double per(double a, double b){
		return a % b;
	}
	
	
}
